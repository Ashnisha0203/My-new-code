package MySecondPro;

public class RelationalOperatorsConcept1 {

	public static void main(String[] args) {

		int a = 20;
		int b= 30;
		
		System.out.println("number 1 is :"+ a);
		System.out.println("number 1 is :"+ b);
		//== operator -->
		
		System.out.println("Check num 1 is equal to num2 "+ (a==b));

		//!= operator -->

		System.out.println("Check num 1 is not equal to num2 "+ (a!=b));

		// > operator -->
		System.out.println("Check num 1 is greater than num2 "+ (a>b));

		// < operator -->

		System.out.println("Check num 1 is less than num2 "+ (a<b));

		// >= operator -->
		System.out.println("Check num 1 is greater than equal to  num2 "+ (a>=b));
		// <= operator -->
		System.out.println("Check num 1 is less than equal to  num2 "+ (a<=b));

		
	}

}
