package MySecondPro;

public class ForLoopExample_2 {

	public static void main(String[] args) {

		//To Print java is my fav lang  100 times -
		
		//for(init,terminator,incremental)
		for(int counter = 0 ; counter <100 ; counter ++) {
			
			System.out.println("java is my fav language");
		}
		
	}

}
